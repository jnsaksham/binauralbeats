var paths;
function gen_seq(files, master_path, shifted_path) {
    var master, paths, shifted;
    paths = list("N");
    for (var fp, _pj_c = 0, _pj_a = files, _pj_b = _pj_a.length; (_pj_c < _pj_b); _pj_c += 1) {
        fp = _pj_a[_pj_c];
        master = (master_path + fp);
        paths.push(master);
        shifted = ((shifted_path + fp.split(".")[0]) + "_shifted.wav");
        paths.push(shifted);
    }
    random.shuffle(paths);
    return paths;
}
