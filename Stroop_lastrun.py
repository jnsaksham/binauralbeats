def seq_randomizer(paths):
    # First arrange in abab
    paths[[1, 2]] = paths[[2, 1]]
    paths = distribute_within(paths)
    return paths

def distribute_within(paths):
    """
    Cases:
    1. i == 0: ABAbBb
    2. i == 1: AbBABb
    3. i == 2: ABbAbB
    4. i == 3: AbBbAB
    """
    i = random.randint(0, 3)
    if i == 0:
        paths = paths
    elif i == 1:
        paths[[0, 2]] = paths[[2, 0]]
    elif i == 2:
        paths[[1, 3]] = paths[[3, 1]]
    else:
        paths[[0, 2]] = paths[[2, 0]]
        paths[[1, 3]] = paths[[3, 1]]
    return paths
    
